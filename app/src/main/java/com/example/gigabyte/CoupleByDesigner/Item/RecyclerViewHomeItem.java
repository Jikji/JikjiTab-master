package com.example.gigabyte.CoupleByDesigner.Item;

public class RecyclerViewHomeItem {

    private String mUrl1;
    private String mUrl2;

    public RecyclerViewHomeItem(String mUrl1, String mUrl2) {
        this.mUrl1 = mUrl1;
        this.mUrl2 = mUrl2;
    }

    public String getmUrl1() {
        return mUrl1;
    }

    public String getmUrl2() {
        return mUrl2;
    }
}
